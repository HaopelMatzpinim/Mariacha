from crypto.encrypt import encrypt_and_sign


def test_encryption():
    assert encrypt_and_sign(b'abc', b'cba') == \
           b'\xbax\x16\xbf\x8f\x01\xcf\xeaAA@\xde]\xae"#\xb0\x03a' \
           b'\xa3\x96\x17z\x9c\xb4\x10\xffa\xf2\x00\x15\xad\x02\x00\x02'

    assert encrypt_and_sign(b'ofndshsodk', b'qwlpotrhjo') == \
           b'l\x94\xf1\xd7;\x13\x12t\x1bR^\x87\xfeg\xc4D+\x81A.' \
           b'\xe9\xb9\xaa \xac\xda\xf3c\xd6\xb8\xed\x01\x1e\x11\x02\x14\x1c\x1c\x01\x07\x0e\x04'
